<h1>My Faith</h1>
<p>
My Faith is very important to me. I'm a Christian, which means I believe that Jesus Christ was the son of God and lived here on earth so that he could give his life for you and me <i>(1 Peter 1:18-19)</i>! This means that, if we believe this and admit the fact that we have sinned, we are free from the guilt that comes with sin and forgiven <i>(Psalm 32:1-5)</i>.
</p>
<p>
This gives us the ability to become more like Him and love others the way He loved everyone <i>(James 1:2-4)</i>.
<b>It's a pretty cool phenomenon to know that the Creator of the whole universe loves me.</b>
</p>
